def binary_to_hex(binary_str):
    # Assurez-vous que la chaîne binaire a une longueur de 24 caractères (3 octets).
    if len(binary_str) != 24:
        raise ValueError("La chaîne binaire doit avoir une longueur de 24 caractères.")
    decimal_value = int(binary_str, 2)
    hex_value = format(decimal_value, '06X')
    hex_color = "#" + hex_value
    
    return hex_color

    
    
